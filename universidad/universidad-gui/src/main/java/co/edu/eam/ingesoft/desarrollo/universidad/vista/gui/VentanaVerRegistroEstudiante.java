    private void btnBuscarEstuainteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarEstuainteActionPerformed
        // TODO add your handling code here:
    	String cedula = tfcedulaestudiante.getText();
    	
    	if(!cedula.isEmpty()){
    		try{
    			Estudiante estudiante = controlador.buscarEstudiante(cedula);
    			
    			if(estudiante != null){
    				tfnombreestudiante.setText(estudiante.getNombre());
    				tfcreditosregistradosestudiante.setText(Long.toString(
        			controlador.calcularCreditosRegistradoPorEstudiante(estudiante).get(0)));
    				cargarTablaCursosRegistrados(controlador.listarRegistrosEstudiante(estudiante));  				
    			}
    		}catch(Exception ex){
    			JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    			ex.printStackTrace();
    		}
    	}
    }//GEN-LAST:event_btnBuscarEstuainteActionPerformed

