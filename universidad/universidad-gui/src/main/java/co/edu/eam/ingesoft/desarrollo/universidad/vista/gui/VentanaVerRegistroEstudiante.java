    public void cargarTablaCursosRegistrados(List<RegistroCurso> listaCursos){
    	DefaultTableModel model = (DefaultTableModel) tablaHorario.getModel();
    	model.setNumRows(0);
    	for(RegistroCurso registroCurso : listaCursos){
    		Object[] fila = new Object[2];
    		fila[0] = registroCurso.getCurso().getAsignatura().getNombre();
    		List<SesionCurso> sesiones = controlador.listarSesiones(registroCurso.getCurso());
    		for(SesionCurso sesion : sesiones){
    			if(sesion.getCurso()==registroCurso.getCurso()){
    				fila[1]=sesion.getDia()+" Hora Inicio: "+sesion.getHoraInicio()
    					+" Hora fin: "+sesion.getHoraFinal();
    			}
    		}
    		model.addRow(fila);
    	}
    }
    
